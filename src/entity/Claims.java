package entity;

public class Claims {
	String insurance_Type;
	int claim_Id;
	int max_claim_amnt;
	String claim_Reason;
	int no_Of_Documents_Required;
	public String getInsurance_Type() {
		return insurance_Type;
	}
	public void setInsurance_Type(String insurance_Type) {
		this.insurance_Type = insurance_Type;
	}
	public int getClaim_Id() {
		return claim_Id;
	}
	public void setClaim_Id(int claim_Id) {
		this.claim_Id = claim_Id;
	}
	public int getMax_claim_amnt() {
		return max_claim_amnt;
	}
	public void setMax_claim_amnt(int max_claim_amnt) {
		this.max_claim_amnt = max_claim_amnt;
	}
	public String getClaim_Reason() {
		return claim_Reason;
	}
	public void setClaim_Reason(String claim_Reason) {
		this.claim_Reason = claim_Reason;
	}
	public int getNo_Of_Documents_Required() {
		return no_Of_Documents_Required;
	}
	public void setNo_Of_Documents_Required(int no_Of_Documents_Required) {
		this.no_Of_Documents_Required = no_Of_Documents_Required;
	}
	
	
	
	
	

}
